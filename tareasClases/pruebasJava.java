package tareasClases;

public class pruebasJava {

	public static void main(String[] args) {
		int y = 144;
		int x = 999;
		
		System.out.println("Valor 1: " + y);
		System.out.println("Valor 2: " + x);

		// SUMA
		int suma = y + x;
		System.out.println("La suma de los dos valores es: " + suma);
		// RESTA
		int resta = y - x;
		System.out.println("La resta de los dos valores es: " + resta);
		// DIVISIÓN
		float division = y / x;
		System.out.println("La división de los dos valores es: " + division);
		// MULTIPLICACIÓN
		int multiplicacion = y * x;
		System.out.println("La multiplicacion de los dos valores es: " + multiplicacion);
		
		System.out.println("--------------------------------");

	}

}
