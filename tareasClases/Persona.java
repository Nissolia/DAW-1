package tareasClases;

public class Persona {
	/* VARIABLES */
	private String nombre;
	private String sexo;
	private int edad;

	/* GETTERS Y SETTERS */
	public String getNombre() {
		// PARA COGER EL NOMBRE
		return nombre;
	}

	public void setNombre(String nombre) {
		// PARA ESCRIBIR
		this.nombre = nombre;
	}

	// SEXO
	public String getSexo() {
		// PARA COGER EL NOMBRE
		return sexo;
	}

	public void setSexo(String sexo) {
		// PARA ESCRIBIR
		this.sexo = sexo;
	}

	// EDAD
	public int getEdad() {
		// PARA COGER EL NOMBRE
		return edad;
	}

	public void setEdad(int edad) {
		// PARA ESCRIBIR
		this.edad = edad;
	}
}
